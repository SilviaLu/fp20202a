package com.upc.curso;

public class App6 {
    public static void main(String[] args) {
        int suma =0;
        for(int i=0; i <10; i++){
            suma+=i;//suma se le conoce como acumulador de valores que toma i (0+1+2+...9)
        }
        System.out.println(suma);//la suma del 0 al 9 => 45
    }
}
